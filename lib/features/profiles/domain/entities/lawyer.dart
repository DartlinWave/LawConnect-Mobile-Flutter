class Lawyer {
  final int id;
  final int userId;
  final String name;
  final String lastName;
  final String dni;
  final String phone;
  final String description;
  final String specialty;
  final String image;
  final double rating;

  Lawyer({
    required this.id,
    required this.userId,
    required this.name,
    required this.lastName,
    required this.dni,
    required this.phone,
    required this.description,
    required this.specialty,
    required this.image,
    required this.rating,
  });
}